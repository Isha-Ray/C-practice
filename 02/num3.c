// Write a program that stores age and marks of a student and prints them.

#include<stdio.h>
int main(){
    int age = 20;
    int marks = 79;

    printf("Student's age: %d\n", age);
    printf("Student's marks: %d\n", marks);
    return 0;
}

/* Output ---------------
    Student's age: 20
    Student's marks: 79
                            */