// Declare and print a string (array of characters) in C.

#include <stdio.h>
int main() {
    char name[] = "Isha Ray";

    printf("My name is: %s\n", name);
    return 0;
}

// Output ---> My name is: Isha Ray


/* A string in C is an array of characters ending with a special null character \0.
    %s is the format specifier used to print strings.
    In C, square brackets are used to declare an array.
*/